function parseCSV(csvString) {
  const lines = csvString.trim().split('\n');
  // –†–µ–≥—É–ª—è—Ä–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –¥–ª—è —Ä–∞–∑–¥–µ–ª–µ–Ω–∏—è CSV, —É—á–∏—Ç—ã–≤–∞—é—â–µ–µ –∫–∞–≤—ã—á–∫–∏
  // –ò—Å—Ç–æ—á–Ω–∏–∫: https://www.oreilly.com/library/view/regular-expressions-cookbook/9780596802837/ch07s02.html
  const csvRegex = /(?:"([^"]*(?:""[^"]*)*)"|([^,]*))/g;

  const parseLine = (line) => {
    const values = [];
    let match;
    while ((match = csvRegex.exec(line)) !== null) {
      // –ï—Å–ª–∏ –µ—Å—Ç—å –≥—Ä—É–ø–ø–∞ 1, –∑–Ω–∞—á–∏—Ç —ç—Ç–æ –∑–Ω–∞—á–µ–Ω–∏–µ –≤ –∫–∞–≤—ã—á–∫–∞—Ö
      if (match[1] !== undefined) {
        values.push(match[1].replace(/""/g, '"')); // –£–¥–∞–ª—è–µ–º —ç–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ –∫–∞–≤—ã—á–µ–∫
      } else if (match[2] !== undefined) {
        values.push(match[2]);
      } else {
        values.push(''); // –ü—É—Å—Ç–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
      }
    }
    return values.map(value => value.trim()); // Trim –∫–∞–∂–¥–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è
  };

  const headers = parseLine(lines[0]);
  const data = [];

  for (let i = 1; i < lines.length; i++) {
    const values = parseLine(lines[i]);
    const row = {};
    for (let j = 0; j < headers.length; j++) {
      row[headers[j]] = values[j] !== undefined ? values[j] : '';
    }
    data.push(row);
  }
  return data;
}

